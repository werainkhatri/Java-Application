public class Dimension {
    private int width;
    private int length;
    private int depth;

    public Dimension(int width, int length, int depth) {
        this.width = width;
        this.length = length;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }

    public int getDepth() {
        return depth;
    }
}
